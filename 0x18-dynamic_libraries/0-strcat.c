#include "main.h"
#include <string.h>

/**
 * _strcat - concatenates two strings
 * @dest: pointer to the string to be concatenated to
 * @src: pointer the string to be concatenated from
 * Return: pointer the concatenated string
 */
char *_strcat(char *dest, char *src)
{
	return (strcat(dest, src));
}
